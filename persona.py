class Persona():

    def __init__(self, nombre, edad, altura, sexo):

        self._nombre = nombre
        self._edad = edad
        self._altura = altura
        self._sexo = sexo
    
    def getNombre(self):
        return self._nombre
      
    def setNombre(self, deporte):
        self._nombre = deporte

    def getEdad(self):
        return self._edad
      
    def setEdad(self, deporte):
        self._edad = deporte
    
    def getAltura(self):
        return self._altura
      
    def setAltura(self, deporte):
        self._altura = deporte
    
    def getSexo(self):
        return self._sexo
      
    def setDeporte(self, deporte):
        self._sexo = deporte